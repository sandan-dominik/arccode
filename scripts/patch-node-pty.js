// Patches node-pty's winpty.gyp to fix build issues on Windows:
// 1. Replaces GetCommitHash.bat call (fails due to working directory)
// 2. Replaces UpdateGenVersion.bat call
// 3. Disables Spectre mitigation (requires extra VS components)
// Also pre-creates GenVersion.h

const fs = require('fs');
const path = require('path');

const winptyGyp = path.join(__dirname, '..', 'node_modules', 'node-pty', 'deps', 'winpty', 'src', 'winpty.gyp');
const bindingGyp = path.join(__dirname, '..', 'node_modules', 'node-pty', 'binding.gyp');
const genDir = path.join(__dirname, '..', 'node_modules', 'node-pty', 'deps', 'winpty', 'src', 'gen');
const genVersion = path.join(genDir, 'GenVersion.h');

if (!fs.existsSync(winptyGyp)) {
  console.log('node-pty winpty.gyp not found, skipping patch');
  process.exit(0);
}

// Patch winpty.gyp
let content = fs.readFileSync(winptyGyp, 'utf-8');
content = content.replace(
  /'\<!\(cmd \/c "cd shared && GetCommitHash\.bat"\)'/,
  "'none'"
);
content = content.replace(
  /'\<!\(cmd \/c "cd shared && UpdateGenVersion\.bat \<\(WINPTY_COMMIT_HASH\)"\)'/,
  "'gen'"
);
content = content.replace(/'SpectreMitigation': 'Spectre'/g, "'SpectreMitigation': 'false'");
fs.writeFileSync(winptyGyp, content);

// Patch binding.gyp
if (fs.existsSync(bindingGyp)) {
  let binding = fs.readFileSync(bindingGyp, 'utf-8');
  binding = binding.replace(/'SpectreMitigation': 'Spectre'/g, "'SpectreMitigation': 'false'");
  fs.writeFileSync(bindingGyp, binding);
}

// Create GenVersion.h
fs.mkdirSync(genDir, { recursive: true });
fs.writeFileSync(genVersion, [
  '// AUTO-GENERATED by patch-node-pty.js',
  'const char GenVersion_Version[] = "0.4.3";',
  'const char GenVersion_Commit[] = "none";',
  '',
].join('\n'));

console.log('node-pty patched successfully');
